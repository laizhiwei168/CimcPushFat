    /*CRC16校验码*/
   	public  int CRC16(byte[] Buf, int len) {  
           int CRC;  
           int i, j;  
           CRC = 0xffff;  
           for (i = 0; i < len; i++) {  
               CRC = CRC ^ (Buf[i] & 0xff);  
               for (j = 0; j < 8; j++) {  
                   if ((CRC & 0x01) == 1)  
                       CRC = (CRC >> 1) ^ 0xA001;  
                   else  
                       CRC = CRC >> 1;  
               }  
           }  
           return CRC;  
       }
   	
